//! # Rust TUI 库文档
//!
//! 这是一个 TUI（终端用户界面）应用程序的库，提供以下主要模块：

/// 参数解析模块
///
/// 处理命令行参数的解析和验证。
pub mod args;

/// 配置管理模块
///
/// 处理应用程序配置的加载、解析和管理。
pub mod config;

/// 调试工具模块
///
/// 提供用于调试的辅助功能，如等待调试器 attach。
pub mod debug;

/// 对话管理模块
///
/// 处理对话会话的创建、保存和加载。
pub mod conversation;

/// LLM 相关模块
///
/// 处理大语言模型的调用、提示词管理和模板渲染。
pub mod llm;

/// Hook 模块
///
/// 处理可配置的外部脚本调用。
pub mod hooks;

/// 模型注册表模块
///
/// 管理可用的 AI 模型配置。
pub mod model_registry;

/// 问题集模块
///
/// 处理问题集的加载和管理。
pub mod question_set;

/// 渲染模块
///
/// 负责将消息和内容渲染为终端可显示的格式，包括 Markdown 渲染、布局管理等。
pub mod render;

/// 框架模块
///
/// 提供与 UI 解耦的基础框架能力，如组件系统等。
pub mod framework;

/// 会话管理模块
///
/// 处理会话的保存、加载和管理。
pub mod session;

/// 类型定义模块
///
/// 定义应用程序中使用的主要数据结构，如消息、角色等。
pub mod types;

/// UI 模块
///
/// 终端用户界面的实现，包括运行时、事件处理、渲染等。
pub mod ui;

/// 服务模块
///
/// 承载非 UI 的服务逻辑，如网络请求、工具执行、容器管理等。
pub mod services;
